package com.eif.ftc.service.transfer.exchange;

public class ExchangeConfig {

	private String transferDomain;
	
	private String accessId;
	
	private String accessKey;
	
	private String applyUrl;
	
	/**
	 * @return the transferDomain
	 */
	public String getTransferDomain() {
		return transferDomain;
	}

	/**
	 * @param transferDomain the transferDomain to set
	 */
	public void setTransferDomain(String transferDomain) {
		this.transferDomain = transferDomain;
	}

	/**
	 * @return the accessId
	 */
	public String getAccessId() {
		return accessId;
	}

	/**
	 * @param accessId the accessId to set
	 */
	public void setAccessId(String accessId) {
		this.accessId = accessId;
	}

	/**
	 * @return the accessKey
	 */
	public String getAccessKey() {
		return accessKey;
	}

	/**
	 * @param accessKey the accessKey to set
	 */
	public void setAccessKey(String accessKey) {
		this.accessKey = accessKey;
	}

	/**
	 * @return the applyUrl
	 */
	public String getApplyUrl() {
		return applyUrl;
	}

	/**
	 * @param applyUrl the applyUrl to set
	 */
	public void setApplyUrl(String applyUrl) {
		this.applyUrl = applyUrl;
	}

}
